int	ft_atoi(const char *str)
{
	int	val;
	int	sign;

	val = 0;
	sign = 1;
	while (*str == ' ' || *str == '\t' || *str == '\n' || *str == '\r'
			|| *str == '\v' || *str == '\f')
		str++;
	if (str[0] == '+' || str[0] == '-')
	{
		if (str[0] == '-')
			sign = -1;
		str++;
	}
	while (str[0] >= '0' && str[0] <= '9')
	{
		val = (val * 10) + (str[0] - '0');
		str++;
	}
	return ((val * sign));
}
